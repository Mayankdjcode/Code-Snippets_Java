// Bitwise Operations
// These operations are performed on the Binary Level . 

class _9_Bitwise 
{
	public static void main(String[] args) 
	{
		int a=10;
		int b=2;
		System.out.println(a&b);  // bitwise AND operator
		System.out.println(a|b);  // bitwise OR
		System.out.println(a^b);  // bitwise XOR
		System.out.println(a<<b); // left shift operator (left shift a in binary form by b number of digits)
		System.out.println(a>>b); // right shift operator (right shift a in binary form by b number of digits)
		
	}
}
